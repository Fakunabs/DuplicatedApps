import UIKit

struct AppFonts {
    static let fontInterBoldItalic = "Inter-BoldItalic"
    
    static func fontInterBoldItalic(size: CGFloat) -> UIFont? {
        return UIFont(name: fontInterBoldItalic, size: size)
    }
}
